import pandas as pd
import datetime
from bs4 import BeautifulSoup
from urllib import request
import json
import re


class HalalStocksFilter:

    def __init__(self):
        self.desc = "Class is HalalScreening for retrieval of data from various websites per stock ticker/symbol in" \
                    " order to perform screening."



    def get_screen_data(self, stocks, unique="No", compression=None, verbose=0, User_Agent=''):

        # Create a dataframe with necessary columns
        df = pd.DataFrame(columns=list(['Ticker',                                                      # 0
                                        'Company Name',                                                # 1
                                        'Company Description',                                         # 2
                                        'Trailing 24-Month Average Market Capitalization (MkCap)',     # 3
                                        'Total Debt (TD)',                                             # 4
                                        'Cash & Short Term Investments (Cash)',                        # 5
                                        'Non-Operating Interest Income (Sec)',                         # 6
                                        'Sum of Cash & Interest-Bearing Securities (Cash+Sec)',        # 7
                                        'Accounts Receivables (AR)',                                   # 8
                                        'TD/MkCap Ratio',                                              # 9
                                        'Cash+Sec/MkCap Ratio',                                        # 10
                                        'AR/MkCap Ratio',                                              # 11
                                        'Fiscal Data Year (TD)',                                       # 12
                                        'Fiscal Data Year (Sec)',                                      # 13
                                        'Fiscal Data Year (Cash)',                                     # 14
                                        'Fiscal Data Year (AR)',                                       # 15
                                        'Description Screening',                                       # 16
                                        'TD/MkCap < 33%',                                              # 17
                                        'CashSec/MkCap < 33%',                                         # 18
                                        'AR/MkCap Ratio < 33%',                                        # 19
                                        'Final Result']))                                              # 20

        compname_tag=0
        # Check if input is a list of stock symbols or is a dataframe with a column of stock symbols
        if isinstance(stocks,list):
            df['Ticker'] = stocks
        elif isinstance(stocks,pd.DataFrame):
            for i in ['Ticker', 'Symbol', 'Stock Symbol', 'Ticker Symbol', 'Stock Ticker']:
                for j in stocks.columns:
                    if (i in j) == True:
                        break
                if (i in j) == True:
                    break
            df['Ticker'] = stocks[j]
            for i in ['Company', 'Company Name', 'Name']:
                for j in stocks.columns:
                    if (i in j) == True:
                        break
                if (i in j) == True:
                    df['Company Name'] = stocks[j]
                    compname_tag=1
                    break
        elif isinstance(stocks,str):
            try:
                stocks = pd.read_csv(stocks)
            except:
                try:
                    stocks = pd.read_csv(stocks, sep=',', header=0, index_col=0, encoding='windows-1252')
                except:
                    try:
                        stocks = pd.read_csv(stocks, sep=',', header=0, index_col=0, encoding='utf-16')
                    except:
                        try:
                            stocks = pd.read_csv(stocks, sep=',', header=0, index_col=0, encoding='ISO-8859-1')
                        except:
                            try:
                                stocks = pd.read_excel(stocks)
                            except:
                                try:
                                    stocks = pd.read_json(stocks)
                                except:
                                    try:
                                        stocks = pd.read_pickle(stocks)
                                    except:
                                        try:
                                            stocks = pd.read_sas(stocks)
                                        except:
                                            try:
                                                stocks = pd.read_sql(stocks)
                                            except:
                                                try:
                                                    stocks = pd.read_sql_query(stocks)
                                                except:
                                                    try:
                                                        stocks = pd.read_sql_table(stocks)
                                                    except:
                                                        try:
                                                            stocks = pd.read_stata(stocks)
                                                        except:
                                                            try:
                                                                stocks = pd.read_parquet(stocks)
                                                            except:
                                                                try:
                                                                    stocks = pd.read_table(stocks)
                                                                except:
                                                                    try:
                                                                        stocks = pd.read_html(stocks)
                                                                    except:
                                                                        raise Exception('Input is neither a list of '
                                                                                        'stock symbols nor a (path to/'
                                                                                        'actual pandas) dataframe with a'
                                                                                        ' stocks symbols column. Please '
                                                                                        're-check in put.')
                
                
                
                
            for i in ['Ticker', 'Symbol', 'Stock Symbol', 'Ticker Symbol', 'Stock Ticker']:
                for j in stocks.columns:
                    if (i in j) == True:
                        break
                if (i in j) == True:
                    break
            df['Ticker'] = stocks[j]
            for i in ['Company', 'Company Name', 'Name']:
                for j in stocks.columns:
                    if (i in j) == True:
                        break
                if (i in j) == True:
                    df['Company Name'] = stocks[j]
                    break
        else:
            raise Exception('Input is neither a list of stock symbols nor a (pandas) dataframe with a stocks symbols'
                            ' column. Please re-check in put.')

        numrows = df.shape[0]
        # Make some folders if nonexistent
        # if not os.path.exists('.\json'):
        #     os.makedirs('.\json')
        # temp_path = '.\json'
        count = 0
        if User_Agent == '':
            User_Agent = 'Mozilla/5.0 (Windows NT 10.0; Win64; x64; rv:61.0) Gecko/20100101 Firefox/61.0'

        for count in range(numrows):
            if verbose >=0:
                print(str(df.iloc[count, 0]) + ": started.")
            ###########################################
            ####     Market Cap (Zacks website)    ####
            ###########################################

            if verbose >= 1:
                print("Acquiring Market Cap for stock number " + str(count+1) + ": "
                    + str(df.iloc[count, 0]) + " from Zacks.")
            tries=0

            while tries<5:
                try:
                    url = "https://widget3.zacks.com/data/chart/json/" + str(
                        df.iloc[count, 0]) + "/market_cap/www.zacks.com"
                    req = request.Request(url, data=None, headers={'User-Agent':User_Agent})
                    r = request.urlopen(req)
                    market_cap = json.load(r)
                    monthly_market_cap = market_cap["monthly_market_cap"]
                    timestamps = list(monthly_market_cap.keys())
                    dates = [datetime.datetime.strptime(ts, "%m/%d/%y") for ts in timestamps]
                    dates.sort(reverse=True)
                    sorteddates = [datetime.datetime.strftime(ts, "%m/%d/%y") for ts in dates]
                    # calculate the 24-month trailing average market cap of company
                    i = 0
                    j = 24
                    mrktcp24 = 0
                    while i < 24:
                        try:
                            mrktcp24 += float(monthly_market_cap[sorteddates[i]])
                        except:
                            mrktcp24 = mrktcp24
                            j -= 1
                        i += 1
                    mrktcp24avg = mrktcp24 / j
                    df.iloc[count, 3] = mrktcp24avg
                    break
                except:
                    tries+=1
                    if tries==5:
                        if verbose >= 2:
                            print("Failed to get Market Cap for stock number " + str(count) + ": "
                                  + str(df.iloc[count, 1]))
                            print("Setting Market Cap for stock number " + str(count)
                                  + " to 0.0000001")
                        df.iloc[count, 3] = 0.0000001

            if verbose >= 1:
                print("Dataframe: " + str(df.iloc[count, 0]) + " data retrieval 33% complete.")

            ###########################################################################################################
            ####      Company Name, Company Description, Cash and Short Term Investments,                         ####
            ####      Non-Operating Interest Income, Accounts Receivable, Total Debt (WSJ and back-up websites)    ####
            ###########################################################################################################

            if verbose >= 2:
                print("Acquiring the Company Name and Description for stock number " + str(count+1) + ": "
                    + str(df.iloc[count, 0]) + " from the WSJ.")
            elif verbose >= 1:
                print("Acquiring the Company Name and Description for stock number " + str(count + 1) + ": "
                      + str(df.iloc[count, 0]))

            ### Get the company 'Description' and the 'Company Name'
            url = ("http://quotes.wsj.com/" + str(df.iloc[count, 0]) + "/company-people")
            tries=0
            while tries<5:
                tries+=1
                ## Get company 'Description'
                try:
                    req = request.Request(url, data=None, headers={'User-Agent': User_Agent})
                    r = request.urlopen(req)
                    soup = BeautifulSoup(r, "html.parser")
                    # Get Description
                    results = soup.find("div", {"class": "cr_description_full cr_expand"}).find("p",{"class": "txtBody"})
                    df.iloc[count, 2] = results.text
                except:
                       df.iloc[count, 2] = "N/A"

                ## If company 'Description' not found on WSJ, try Zacks
                url = ("https://www.zacks.com/stock/quote/" + str(df.iloc[count, 0]))
                if df.iloc[count, 2] == "N/A":
                    try:
                        req = request.Request(url, data=None, headers={'User-Agent': User_Agent})
                        r = request.urlopen(req)
                        soup = BeautifulSoup(r, "html.parser")
                        # Get Description
                        results = soup.find("section", {"id": "stock_comp_desc"}).find("p")
                        df.iloc[count, 2] = results.text
                    except:
                        df.iloc[count, 2] = "N/A"

                ## Get the 'Company Name'
                if compname_tag==0:
                    try:
                        req = request.Request(url, data=None, headers={'User-Agent': User_Agent})
                        r = request.urlopen(req)
                        soup = BeautifulSoup(r, "html.parser")
                        # Get Name
                        results = soup.find("div", {"class": "quote_summary"}).find("h1")
                        df.iloc[count, 1] = results.text.split("(")[0]
                    except:
                           df.iloc[count, 1] = "N/A"
                break


            if verbose >= 2:
                print("Acquiring the Total Debt for stock number " + str(count+1) + ": "
                    + str(df.iloc[count, 0]) + " from the WSJ.")
            elif verbose >= 1:
                print("Acquiring the Total Debt for stock number " + str(count + 1) + ": "
                        + str(df.iloc[count, 0]))

            ### Get the 'Total Debt'
            tries=0
            url = "http://quotes.wsj.com/" + str(df.iloc[count, 0]) + "/financials"
            while tries < 5:
                tries+=1
                try:
                    req = request.Request(url, data=None, headers={'User-Agent': User_Agent})
                    r = request.urlopen(req)
                    soup = BeautifulSoup(r, "html.parser")
                    break
                except:
                    if verbose >= 2:
                        print("Attempt " + str(tries + 1) + " at retrieving the 'Total Debt' from WSJ")

            try:
                check = soup.find("section", {"class": "sector cr_section_2"}).find("span", {"class": "data_none"}).text
            except:
                check = ''

            FiscalDataYear3 = '0000'
            tries=0
            if check != 'Data not available.':
                while tries < 5:
                    try:
                        results = soup.find("div", {"data-module-id": "8"}).find("table", {
                            "class": "cr_financials_table"}).find("tbody").find_next_sibling().find("tr", {
                            "class": "cr_financials_row"}).find_all("th")
                        FiscalDataYear3 = results[1].text
                        results = soup.find("div", {"data-module-id": "8"}).find("table", {
                            "class": "cr_financials_table"}).find("tbody").find_next_sibling().find("tr", {
                            "class": "cr_financials_row"}).find_next_siblings()
                        try:
                            match1 = re.search(r'\sBi*', results[1].text).group()[1]
                        except AttributeError:
                            match1 = False
                        try:
                            match2 = re.search(r'\sMi*', results[1].text).group()[1]
                        except AttributeError:
                            match2 = False
                        if match1:
                            value = 1000.0
                        elif match2:
                            value = 1.0
                        else:
                            value = 1.0 / 1000
                        try:
                            df.iloc[count, 4] = float(re.search(r'\d+\.*\d*', results[1].text).group()) * value
                        except:
                            df.iloc[count, 4] = 0
                        break
                    except:
                        tries+=1
                        if tries==5:
                            df.iloc[count, 4] = 'N/A'
            else:
                df.iloc[count, 4] = "N/A"

            # If Total Debt not found on WSJ, try Zacks
            if df.iloc[count, 4] == 'N/A':
                if verbose >= 2:
                    print("Total Debt not found on WSJ. Attempting to retrieve sum of ['Current Portion Long-Term Debt' "
                          "+ 'Long-Term Debt' + 'Convertible Debt'] from Zacks.")
                tries=0
                while tries<5:
                    try:
                        url = "https://www.zacks.com/stock/quote/" + str(df.iloc[count, 0]) + "/balance-sheet"
                        req = request.Request(url, data=None, headers={'User-Agent': User_Agent})
                        r = request.urlopen(req)
                        soup = BeautifulSoup(r, "html.parser")
                        results = soup.find("div", {"id": "annual_income_statement"}).find("table").find("thead").find("tr").find_all("th")[1].text
                        FiscalDataYear3 = results.split('/')[2]
                        results = soup.find("div", {"id": "annual_income_statement"}).find_all("p")[1].text
                        try:
                            match1 = re.search(r'\sBi*', results).group()[1]
                        except AttributeError:
                            match1 = False
                        try:
                            match2 = re.search(r'\sMi*', results).group()[1]
                        except AttributeError:
                            match2 = False
                        if match1:
                            value = 1000.0
                        elif match2:
                            value = 1.0
                        else:
                            value = 1.0 / 1000
                        results = soup.find("div", {"id": "annual_income_statement"}).find("table").find_next("table").find(
                            "tbody").find_all("tr")
                        count2 = 0
                        totalDebt = 0
                        while count2 < len(results):
                            name = results[count2].find("td").text
                            if name == 'Current Portion Long-Term Debt':
                                totalDebt += float(results[count2].find_all("td")[1].text.replace(",", ""))
                            elif name == 'Long-Term Debt':
                                totalDebt += float(results[count2].find_all("td")[1].text.replace(",", ""))
                            elif name == 'Convertible Debt':
                                totalDebt += float(results[count2].find_all("td")[1].text.replace(",", ""))
                            count2 += 1
                        try:
                            df.iloc[count, 4] = totalDebt * value
                        except:
                            df.iloc[count, 4] = 0
                        break
                    except:
                        tries+=1


            ### Get the latest Fiscal Year, 'Cash and Short Term Investments', and 'Total Accounts Receivable'
            if verbose >= 2:
                print("Acquiring the Cash and Short Term Investments, and Total Accounts Receivable, for latest Fiscal"
                      " Year available, for stock number " + str(count + 1) + ": " + str(df.iloc[count, 0]) + " from the WSJ.")
            elif verbose >= 1:
                print("Acquiring the Cash and Short Term Investments, and Total Accounts Receivable, for latest Fiscal"
                      " Year available, for stock number " + str(count + 1) + ": " + str(
                    df.iloc[count, 0]))
            url = "http://quotes.wsj.com/" + str(df.iloc[count, 0]) + "/financials/annual/balance-sheet"
            tries=0
            while tries<5:
                try:
                    req = request.Request(url, data=None, headers={'User-Agent': User_Agent})
                    r = request.urlopen(req)
                    soup = BeautifulSoup(r, "html.parser")
                    # Get latest Fiscal Year on website
                    try:
                        results = soup.find("table", {"class": "cr_dataTable"}).find("thead").find("tr").find_all("th")
                        FiscalDataYear1 = results[1].text
                    except:
                        FiscalDataYear1 = '0000'

                    # Get the base of values: in thousands, or in millions
                    if FiscalDataYear1 != '0000':
                        BaseValue = results[0].text
                        try:
                            match1 = re.search(r'thousands', BaseValue.lower())
                        except:
                            match1 = False
                        try:
                            match2 = re.search(r'millions', BaseValue.lower())
                        except:
                            match2 = False
                        try:
                            match3 = re.search(r'billions', BaseValue.lower())
                        except:
                            match3 = False
                        if match1:
                            value = 1000.0
                        elif match2:
                            value = 1.0
                        elif match3:
                            value = 1.0 / 1000
                    break
                except:
                    tries += 1
                    if tries == 5:
                        FiscalDataYear1 = '0000'

            tries=0
            while tries<5:
                try:
                    # Get the rest of the data
                    results = soup.find("table", {"class": "cr_dataTable"}).find("tbody").find_all("tr")

                    # Get 'Cash and Short Term Investments'
                    num = results[2].findChildren("td")[1].text
                    try:
                        df.iloc[count, 5] = float(num.replace(",", "")) / value
                    except ValueError:
                        df.iloc[count, 5] = 'N/A'

                    # Get 'Total Accounts Receivable'
                    try:
                       num = results[13].findChildren("td")[1].text
                       try:
                           df.iloc[count, 8] = float(num.replace(",", "")) / value
                       except ValueError:
                           df.iloc[count, 8] = 'N/A'
                    except:
                       df.iloc[count, 8] = 'N/A'
                    break
                except:
                    tries+=1
                    if tries==5:
                        df.iloc[count, 5] = 'N/A'
                        df.iloc[count, 8] = 'N/A'

